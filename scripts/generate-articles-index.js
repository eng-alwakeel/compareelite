#!/usr/bin/env node
/**
 * Generate data/articles-index.md — categorized list of every published
 * article. Used by the CMO skill to avoid topic duplication and to pick
 * valid slugs for `related_articles`.
 *
 *   node scripts/generate-articles-index.js
 */

'use strict';

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const ARTICLES_DIR = path.join(ROOT, 'articles');
const OUT_PATH = path.join(ROOT, 'data', 'articles-index.md');

const VALID_CATEGORIES = ['Tech', 'Home Office', 'Smart Home', 'Home Fitness'];

function loadArticles() {
  const files = fs
    .readdirSync(ARTICLES_DIR)
    .filter((f) => f.endsWith('.json') && f !== 'TEMPLATE.json')
    .sort();
  const articles = [];
  for (const f of files) {
    const slug = path.basename(f, '.json');
    try {
      const data = JSON.parse(fs.readFileSync(path.join(ARTICLES_DIR, f), 'utf8'));
      articles.push({
        slug,
        title: typeof data.title === 'string' ? data.title : slug,
        category: typeof data.category === 'string' ? data.category : 'Uncategorized',
      });
    } catch (err) {
      console.error(`skip ${f}: ${err.message}`);
    }
  }
  return articles;
}

function render(articles) {
  const today = new Date().toISOString().slice(0, 10);
  const byCategory = new Map();
  for (const c of VALID_CATEGORIES) byCategory.set(c, []);
  const uncategorized = [];
  for (const a of articles) {
    if (byCategory.has(a.category)) byCategory.get(a.category).push(a);
    else uncategorized.push(a);
  }

  const lines = [];
  lines.push('# CompareElite — Published Articles Index');
  lines.push('');
  lines.push(`Last updated: ${today}`);
  lines.push(`Total articles: ${articles.length}`);
  lines.push('');
  lines.push('Generated by `scripts/generate-articles-index.js`. Do not edit by hand.');
  lines.push('');

  for (const cat of VALID_CATEGORIES) {
    const rows = byCategory.get(cat).sort((a, b) => a.slug.localeCompare(b.slug));
    lines.push(`## ${cat}`);
    lines.push('');
    if (rows.length === 0) {
      lines.push('_(none yet)_');
    } else {
      for (const a of rows) lines.push(`- \`${a.slug}\` | ${a.title}`);
    }
    lines.push('');
  }

  if (uncategorized.length > 0) {
    lines.push('## Uncategorized (NOT in approved set)');
    lines.push('');
    for (const a of uncategorized) {
      lines.push(`- \`${a.slug}\` | ${a.title} _(category: ${a.category})_`);
    }
    lines.push('');
  }

  lines.push('## Topics to AVOID (already covered)');
  lines.push('');
  lines.push('Do not write a new article on any topic in this list. Pick a different angle, a different product class, or a different category.');
  lines.push('');
  for (const a of articles.slice().sort((x, y) => x.title.localeCompare(y.title))) {
    lines.push(`- ${a.title}`);
  }
  lines.push('');

  return lines.join('\n') + '\n';
}

function main() {
  const articles = loadArticles();
  const md = render(articles);
  fs.mkdirSync(path.dirname(OUT_PATH), { recursive: true });
  fs.writeFileSync(OUT_PATH, md, 'utf8');
  console.log(`Wrote ${path.relative(ROOT, OUT_PATH)} — ${articles.length} articles`);
}

if (require.main === module) main();

module.exports = { loadArticles, render };
